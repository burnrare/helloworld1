package co.edu.abstracts;

public class Avante {
	public void powerOn() {
		System.out.println("시동이 켜졌습니다.");
	}

	public void startUp() {
		System.out.println("출발합니다.");
	}

	public void running() {
		System.out.println("운전 중입니다.");
	}

	public void stopping() {
		System.out.println("운전을 멈췄습니다.");
	}

	public void powerOff() {
		System.out.println("시동이 꺼졌습니다.");
	}
}
